#include<iostream.h>
#include<math.h>
#include<conio.h>

void main()
{
    int x,n;
    float sum=0;

    cout<<"x + x^2/2 + x^3/3...x^n/n";
    cout<<"\nEnter Value of x : ";
    cin>>x;
    cout<<"\nEnter No of terms : ";
    cin>>n;

    for(int i=1;i<=n;i++)
    {
        sum+=pow(x,i)/i;
    }

    cout<<"\nSum is "<<sum<<endl;

    getch();
}
